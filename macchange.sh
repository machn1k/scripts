length=${#prefix[*]}

osx () {
for i in ${interfaces[*]} 
{
    # This will generate an error... ignore it.
    hw_prefix=${prefix[$(shuf -i 0-"$length" -n 1)]}
    hw_suffix=$(for i in $(seq 1 6); do echo -n $(echo "obase=16; $(($RANDOM % 16))" | bc); done; echo)
    hw_addr=$(echo "$hw_prefix$hw_suffix" | sed "s/.\{2\}/&:/g")
    
    echo "Changing MAC address to "${hw_addr%?}
    sudo ifconfig $i ether ${hw_addr%?}
    echo "Bringing down interface $i..."
    sudo ifconfig $i down
    echo "Bringing $i back up..."
    sudo ifconfig $i up
    
    echo ""
  }
}

linux () {
for i in ${interfaces[*]} 
{
    hw_prefix=${prefix[$(shuf -i 0-"$length" -n 1)]}
    hw_suffix=$(for i in $(seq 1 6); do echo -n $(echo "obase=16; $(($RANDOM % 16))" | bc); done; echo)
    hw_addr=$(echo "$hw_prefix$hw_suffix" | sed "s/.\{2\}/&:/g")
    
    echo "Bringing down interface $i..."
    sudo ip link set dev $i down
    echo "Changing MAC address to "${hw_addr%?}
    sudo ip link set dev $i address ${hw_addr%?}
    echo "Bringing $i back up..."
    sudo /etc/init.d/networking restart
    #sudo ip link set dev $i up
    
    echo ""
    }
}

zarg () {
    echo -e "\tUsage: randMAC [interface 1] [interface 2]"
    exit
}

